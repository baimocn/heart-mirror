// 询问用户选择理由


async function selectOption(optIndex, btnElement) {
    if (isFetchingAI) return;
    

    
    // 切换计数
    if (currentOptionSelected[currentQuestionIndex] !== -1 && currentOptionSelected[currentQuestionIndex] !== optIndex) {
        questionSwitchCount[currentQuestionIndex]++;
    }
    currentOptionSelected[currentQuestionIndex] = optIndex;
    const q = currentExam[currentQuestionIndex];
    const opt = q.options[optIndex];
    answers[currentQuestionIndex] = {
        increments: opt.increments,
        reflection: opt.reflection || '',
        selected_idx: optIndex,
        duration: null,   // 结束时填充
        switches: questionSwitchCount[currentQuestionIndex],
        reason: null      // 选择理由
    };
    // 高亮按钮和更新ARIA属性
    document.querySelectorAll('.option-btn').forEach(function(btn, idx) {
        if (idx === optIndex) {
            btn.classList.add('selected');
            btn.setAttribute('aria-checked', 'true');
        } else {
            btn.classList.remove('selected');
            btn.setAttribute('aria-checked', 'false');
        }
    });
    
    // 检查是否触发 AI 对话
    if (opt.ai_trigger === true) {
        // 临时禁用下一题按钮，等待用户看完 AI 回响
        const nextBtn = document.getElementById('nextBtn');
        if (nextBtn) nextBtn.disabled = true;
        
        isFetchingAI = true;
        const aiMessage = await fetchAIReflection(q.text, opt.text);
        // 显示模态框
        showAIDialog(aiMessage, function() {
            // 启用下一题按钮
            if (nextBtn) {
                nextBtn.disabled = false;
                // 设置焦点到下一题按钮
                nextBtn.focus();
            }
            isFetchingAI = false;
        });
    } else {
        // 没有 AI 触发，直接启用下一题
        const nextBtn = document.getElementById('nextBtn');
        nextBtn.disabled = false;
        // 设置焦点到下一题按钮
        nextBtn.focus();
    }
    
    // 动态追问检测（在每次选择后立即检测累积模式）
    detectAndProbe();
}

// 动态追问：检测是否触发规则，若触发则弹出模态框（不阻塞答题，但记录回答）
async function detectAndProbe() {
    // 收集已完成的选择（只包括已回答的题目）
    let completedAnswers = answers.filter(function(a) { return a !== undefined; });
    if (completedAnswers.length < 3) return;
    // 临时计算当前倾向（基于已选题目）
    let tempScores = DIMENSIONS.map(function(d) { return 0; });
    for (let i = 0; i < completedAnswers.length; i++) {
        let ans = completedAnswers[i];
        for (let j = 0; j < DIMENSIONS.length; j++) {
            tempScores[j] += ans.increments[DIMENSIONS[j]] || 0;
        }
    }
    let normTemp = tempScores.map(function(v) { return Math.min(100, Math.max(0, (v+50)/100*100)); });
    for (let i = 0; i < PROBE_RULES.length; i++) {
        let rule = PROBE_RULES[i];
        if (rule.condition(normTemp, completedAnswers)) {
            // 避免重复追问同一规则（记录已问过）
            if (!patternsDetected.includes(rule.message)) {
                patternsDetected.push(rule.message);
                await showProbeModal(rule.message);
                break; // 每次选择只问一条
            }
        }
    }
}

