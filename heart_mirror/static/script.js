async function loadCoverData() {
    try {
        const res = await fetch('/api/cover_data');
        const data = await res.json();
        document.getElementById('coverQuote').innerText = data.quote;
        // 动态改变背景渐变色
        const overlay = document.querySelector('.opening-overlay');
        overlay.style.background = `radial-gradient(circle at center, ${data.bg_start}, ${data.bg_end})`;
        // 改变镜面光晕颜色
        const mirrorFace = document.querySelector('.mirror-face');
        if (mirrorFace) {
            mirrorFace.style.boxShadow = `0 0 50px ${data.glow_color}`;
        }
    } catch(e) {
        console.warn('加载封面数据失败', e);
    }
}

