        for (let i = 0; i < answers.length; i++) {
            let ans = answers[i];
            for (let j = 0; j < DIMENSIONS.length; j++) {
                let d = DIMENSIONS[j];
                total[d] += ans.increments[d];
            }
        }
        let scores = DIMENSIONS.map(function(d) { return Math.min(100, Math.max(0, (total[d]+50)/100*100)); });
