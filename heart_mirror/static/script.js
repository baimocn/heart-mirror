// 开始音乐
function startMusic(mood) {
    if (isMusicMuted) return;
    try {
        if (!audioContext) {
            audioContext = new (window.AudioContext || window.webkitAudioContext)();
        }
        const params = moodMusicMap[mood] || moodMusicMap.default_mood;
        currentOscillator = audioContext.createOscillator();
        currentGain = audioContext.createGain();
        currentOscillator.type = params.type;
        currentOscillator.frequency.setValueAtTime(params.frequency, audioContext.currentTime);
        currentGain.gain.setValueAtTime(params.volume, audioContext.currentTime);
        currentOscillator.connect(currentGain);
        currentGain.connect(audioContext.destination);
        currentOscillator.start();
        // 自动停止
        setTimeout(function() {
            stopMusic();
        }, params.duration);
    } catch (e) {
        console.error('播放音乐失败', e);
    }
}

// 停止音乐
function stopMusic() {
    if (currentOscillator) {
        try {
            currentOscillator.stop();
        } catch (e) {
            console.error('停止音乐失败', e);
        }
        currentOscillator = null;
    }
    if (currentGain) {
        currentGain.disconnect();
        currentGain = null;
    }
}

// 根据mood切换音乐
function switchMusicByMood(mood) {
    if (isMusicPlaying) {
        stopMusic();
        startMusic(mood);
    }
}

