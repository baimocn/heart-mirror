// 从 questions.json 构建类别映射，然后随机抽题
let questionsByCategory = {};
let isQuickTest = false;
let isDeepTest = false;
async function loadQuestions(quickTest) {
    isQuickTest = quickTest;
    // 添加时间戳和随机数防止缓存
    const res = await fetch('/api/questions?_=' + Date.now() + Math.random());
    const data = await res.json();  // 格式：{ categories: [...], questions: [...] }
    questionsByCategory = {};
    // 按 category 分组
    data.questions.forEach(function(q) {
        if (!questionsByCategory[q.category]) questionsByCategory[q.category] = [];
        questionsByCategory[q.category].push(q);
    });
    buildRandomExam();
    renderQuestion();
    updateStats();
}

function buildRandomExam() {
    let exam = [];
    const cats = ['justice','altruism','rule','empathy','utilitarian','punish'];
    
    console.log('开始生成题目，isQuickTest:', isQuickTest);
    console.log('各类别题目数量:', Object.entries(questionsByCategory).map(([cat, qs]) => `${cat}: ${qs.length}`));
    
    if (isQuickTest) {
        // 快速测试：从每个类别中随机选择1题，共6题，然后再随机选择3题
        let allQuestions = [];
        for (let i = 0; i < cats.length; i++) {
            let cat = cats[i];
            let pool = questionsByCategory[cat];
            if (pool && pool.length > 0) {
                let randomQuestion = pool[Math.floor(Math.random() * pool.length)];
                allQuestions.push(randomQuestion);
            }
        }
        // 打乱顺序
        for (let i = allQuestions.length - 1; i > 0; i--) {
            let j = Math.floor(Math.random() * (i + 1));
            let temp = allQuestions[i];
            allQuestions[i] = allQuestions[j];
            allQuestions[j] = temp;
        }
        // 选择前3题
        exam = allQuestions.slice(0, 3);
    } else {
        // 根据测试模式确定每类题目数量
        let questionsPerCategory = isDeepTest ? 13 : 4; // 深度测试每类13题，普通测试每类4题
        
        // 深度测试或普通测试：每类抽题
        for (let i = 0; i < cats.length; i++) {
            let cat = cats[i];
            let pool = questionsByCategory[cat];
            if (!pool || pool.length === 0) continue;
            // 使用 Fisher-Yates 洗牌
            let shuffled = [...pool];
            for (let i = shuffled.length - 1; i > 0; i--) {
                let j = Math.floor(Math.random() * (i + 1));
                let temp = shuffled[i];
                shuffled[i] = shuffled[j];
                shuffled[j] = temp;
            }
            // 每类抽尽可能多的题目
            let takeCount = Math.min(pool.length, questionsPerCategory);
            exam.push(...shuffled.slice(0, takeCount));
        }
        
        // 深度测试：添加额外题目达到81题
        if (isDeepTest) {
            let allQs = Object.values(questionsByCategory).flat();
            while (exam.length < 81 && allQs.length > 0) {
                let randomIndex = Math.floor(Math.random() * allQs.length);
                exam.push(allQs[randomIndex]);
                // 从allQs中移除已选择的题目
                allQs.splice(randomIndex, 1);
            }
        }
        
        // 普通测试：确保至少有25题
        if (!isDeepTest && exam.length < 25) {
            let allQs = Object.values(questionsByCategory).flat();
            while (exam.length < 25 && allQs.length > 0) {
                let randomIndex = Math.floor(Math.random() * allQs.length);
                exam.push(allQs[randomIndex]);
                // 从allQs中移除已选择的题目
                allQs.splice(randomIndex, 1);
            }
        }
        
        // 再次打乱顺序
        for (let i = exam.length - 1; i > 0; i--) {
            let j = Math.floor(Math.random() * (i + 1));
            let temp = exam[i];
            exam[i] = exam[j];
            exam[j] = temp;
        }
    }
    
    currentExam = exam;
    console.log('生成的题目数量:', currentExam.length);
    answers = new Array(currentExam.length);
    questionStartTime = new Array(currentExam.length);
    questionSwitchCount = new Array(currentExam.length).fill(0);
    currentOptionSelected = new Array(currentExam.length).fill(-1);
}

