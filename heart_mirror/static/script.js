// 主题切换功能
let currentTheme = 'light';

function initTheme() {
    // 从localStorage读取保存的主题偏好
    const savedTheme = localStorage.getItem('heartMirror_theme');
    if (savedTheme) {
        currentTheme = savedTheme;
    } else {
        // 自动检测系统主题
        if (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches) {
            currentTheme = 'dark';
        }
    }
    applyTheme(currentTheme);
}

function applyTheme(theme) {
    const appContainer = document.querySelector('.app-container');
    if (!appContainer) return;
    
    // 移除所有主题类
    appContainer.classList.remove('light-mode', 'dark-mode');
    
    // 添加当前主题类
    if (theme === 'dark') {
        appContainer.classList.add('dark-mode');
        document.getElementById('themeToggle').innerHTML = '<i class="fas fa-sun"></i> 主题';
    } else {
        appContainer.classList.add('light-mode');
        document.getElementById('themeToggle').innerHTML = '<i class="fas fa-moon"></i> 主题';
    }
    
    // 保存主题偏好到localStorage
    localStorage.setItem('heartMirror_theme', theme);
    currentTheme = theme;
}

function toggleTheme() {
    const newTheme = currentTheme === 'light' ? 'dark' : 'light';
    applyTheme(newTheme);
}

// 初始化
async function init() {
    // 初始化粒子背景
    initParticles();
    // 加载封面数据
    loadCoverData();
    // 初始化主题
    initTheme();
    // 更新统计数据
    await updateStats();
    // 绑定深度镜渊测试按钮（81题）
    document.getElementById('deepTestBtn').onclick = function() {
        document.querySelector('.opening-overlay').style.display = 'none';
        document.querySelector('.app-container').style.display = 'block';
        // 设置为深度测试模式
        isDeepTest = true;
        loadQuestions(false);
    };
    // 绑定心镜常照测试按钮（25题）
    document.getElementById('normalTestBtn').onclick = function() {
        document.querySelector('.opening-overlay').style.display = 'none';
        document.querySelector('.app-container').style.display = 'block';
        // 设置为普通测试模式
        isDeepTest = false;
        loadQuestions(false);
    };
    // 绑定一瞥心光测试按钮（3题）
    document.getElementById('quickTestBtn').onclick = function() {
        document.querySelector('.opening-overlay').style.display = 'none';
        document.querySelector('.app-container').style.display = 'block';
        // 设置为快速测试模式
        isDeepTest = false;
        loadQuestions(true);
    };
    // 绑定音乐切换按钮
    const toggleMusicBtn = document.getElementById('musicToggle');
    if (toggleMusicBtn) {
        toggleMusicBtn.onclick = toggleMusic;
    }
    // 绑定主题切换按钮
    const themeToggleBtn = document.getElementById('themeToggle');
    if (themeToggleBtn) {
        themeToggleBtn.onclick = toggleTheme;
    }
}

