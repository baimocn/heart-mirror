// 哈希函数 - 生成8位十六进制指纹
// 生成唯一指纹
function generateFingerprint(answers) {
  // 按照要求，使用题目ID和选项索引的组合
  const str = answers.map((a, idx) => `${currentExam[idx].id}:${a.selected_idx}`).join('|');
  // 简单的哈希算法
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash) + str.charCodeAt(i);
    hash |= 0; // 转换为32位整数
  }
  // 转换为8位十六进制字符串
  const fingerprint = (hash >>> 0).toString(16).padStart(8, '0').substring(0, 8);
  console.log('生成的指纹:', fingerprint);
  return fingerprint;
}

